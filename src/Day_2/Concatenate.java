package Day_2;

public class Concatenate {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        String text = "14";
        System.out.println(a + b);
        System.out.println(text + a);
        long h = 7879;
        int c = (int)h;
        char t = 'a';
        short r = (short)t;
    }
}
